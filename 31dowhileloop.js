let hitung = 100;
do {
  console.log("Perulangan ke-" + hitung);
  hitung++;
} while (hitung <= 10);
// bedanya for loop, while loop, dan do while loop adalah:
// - for loop, kondisi di cek di awal sebelum perulangan dijalankan
// - while loop, kondisi di cek di awal sebelum perulangan dijalankan dan jika kondisi tidak terpenuhi maka perulangan tidak dijalankan sama sekali (0 kali)
// - do while loop, kondisi di cek di akhir setelah perulangan dijalankan dan jika kondisi tidak terpenuhi maka perulangan dijalankan minimal 1 kali
